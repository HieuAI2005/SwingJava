    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) studentTable.getModel();
        
        String id = tblModel.getValueAt(studentTable.getSelectedRow(),0).toString();
        String last_name = tblModel.getValueAt(studentTable.getSelectedRow(),1).toString();
        String first_name = tblModel.getValueAt(studentTable.getSelectedRow(),2).toString();
        String birthday = tblModel.getValueAt(studentTable.getSelectedRow(),3).toString();
        String gender = tblModel.getValueAt(studentTable.getSelectedRow(),4).toString();
        String phone = tblModel.getValueAt(studentTable.getSelectedRow(),5).toString();
        String email = tblModel.getValueAt(studentTable.getSelectedRow(),6).toString();
        String classroom = tblModel.getValueAt(studentTable.getSelectedRow(),7).toString();
        
        this.idText.setText(id);
        this.last_nameTextStudent.setText(last_name);
        this.first_nameText.setText(first_name);
        this.birthdayText.setText(birthday);
        if(gender.equals("Male")){
            this.gender1Text.setSelected(true);
        }else{
            this.gender2Text.setSelected(true);
        }
        this.phoneText.setText(phone);
        this.emailText.setText(email);
        this.classroomText.setText(classroom);
    }//GEN-LAST:event_studentTableMouseClicked

