    public ModelStudent getInformation(){
        ModelStudent student;
        
        String id = this.idText.getText();
        String last_name = this.last_nameTextStudent.getText();
        String first_name = this.first_nameText.getText();
        String birthday = this.birthdayText.getText();
        String gender; 
        if(gender1Text.isSelected()) gender = gender1Text.getText();
        else gender = gender2Text.getText();
        String phone = this.phoneText.getText();
        String email = this.emailText.getText();
        String id_classroom = this.classroomText.getText();
        student = new ModelStudent(id,last_name,first_name,birthday,gender,phone,email,id_classroom);
        
        return student;
    }
    
    public void setInformation(ModelStudent student){
        this.idText.setText(student.getId());
        this.last_nameTextStudent.setText(student.getLast_name());
        this.first_nameText.setText(student.getFirst_name());
        if(student.getGender().equals("Male")){
            this.gender1Text.setSelected(true);
        }else{
            this.gender2Text.setSelected(true);
        }
        this.birthdayText.setText(student.getBirthday());
        this.emailText.setText(student.getEmail());
        this.phoneText.setText(student.getPhone());
        this.classroomText.setText(student.getId_classroom());
    }
    
