    private void teacherTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_teacherTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) teacherTable.getModel();
        
        String id = tblModel.getValueAt(teacherTable.getSelectedRow(),0).toString();
        String last_name = tblModel.getValueAt(teacherTable.getSelectedRow(),1).toString();
        String first_name = tblModel.getValueAt(teacherTable.getSelectedRow(),2).toString();
        String birthday = tblModel.getValueAt(teacherTable.getSelectedRow(),3).toString();
        String gender = tblModel.getValueAt(teacherTable.getSelectedRow(),4).toString();
        String phone = tblModel.getValueAt(teacherTable.getSelectedRow(),5).toString();
        String email = tblModel.getValueAt(teacherTable.getSelectedRow(),6).toString();
        String classroom = tblModel.getValueAt(teacherTable.getSelectedRow(),7).toString();
        
        this.idText.setText(id);
        this.last_nameText.setText(last_name);
        this.first_nameText.setText(first_name);
        this.birthdayText.setText(birthday);
        if(gender.equals("Male")){
            this.gender1Text.setSelected(true);
        }else{
            this.gender2Text.setSelected(true);
        }
        this.phoneText.setText(phone);
        this.emailText.setText(email);
        this.classroomText.setText(classroom);
    }//GEN-LAST:event_teacherTableMouseClicked

