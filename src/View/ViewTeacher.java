    public void setInformation(ModelTeacher teacher){
        this.idText.setText(teacher.getId());
        this.last_nameText.setText(teacher.getLast_name());
        this.first_nameText.setText(teacher.getFirst_name());
        if(teacher.getGender().equals("Male")){
            this.gender1Text.setSelected(true);
        }else{
            this.gender2Text.setSelected(true);
        }
        this.birthdayText.setText(teacher.getBirthday());
        this.emailText.setText(teacher.getEmail());
        this.phoneText.setText(teacher.getPhone());
        this.classroomText.setText(teacher.getId_classroom());
    }
    
    public ModelTeacher getInformation(){
        ModelTeacher teacher; 
        
        String id = this.idText.getText();
        String last_name = this.last_nameText.getText();
        String first_name = this.first_nameText.getText();
        String birthday = this.birthdayText.getText();
        String gender; 
        if(gender1Text.isSelected()) gender = gender1Text.getText();
        else gender = gender2Text.getText();
        String phone = this.phoneText.getText();
        String email = this.emailText.getText();
        String id_classroom = this.classroomText.getText();
        teacher = new ModelTeacher(id,last_name,first_name,birthday,gender,phone,email,id_classroom);
        
        return teacher;
    }
    
